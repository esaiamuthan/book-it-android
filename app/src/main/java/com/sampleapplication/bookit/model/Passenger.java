package com.sampleapplication.bookit.model;

public class Passenger {

    public String pName;
    public long pAge;
    public long pGender;
    public String pProof;
    public String pProofId;
    public long pProofPosition;
    public String berth;
    public long pBerthPosition;

    public String getpName() {
        return pName;
    }

    public void setpName(String pName) {
        this.pName = pName;
    }

    public long getpAge() {
        return pAge;
    }

    public void setpAge(long pAge) {
        this.pAge = pAge;
    }

    public long getpGender() {
        return pGender;
    }

    public void setpGender(long pGender) {
        this.pGender = pGender;
    }

    public String getpProof() {
        return pProof;
    }

    public void setpProof(String pProof) {
        this.pProof = pProof;
    }

    public String getpProofId() {
        return pProofId;
    }

    public void setpProofId(String pProofId) {
        this.pProofId = pProofId;
    }

    public long getpProofPosition() {
        return pProofPosition;
    }

    public void setpProofPosition(long pProofPosition) {
        this.pProofPosition = pProofPosition;
    }

    public String getBerth() {
        return berth;
    }

    public void setBerth(String berth) {
        this.berth = berth;
    }

    public long getpBerthPosition() {
        return pBerthPosition;
    }

    public void setpBerthPosition(long pBerthPosition) {
        this.pBerthPosition = pBerthPosition;
    }
}
